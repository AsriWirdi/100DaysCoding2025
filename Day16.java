public class Day16 {
  public static void main(String[] args) {

    /*
     * 100 Days Of Coding 2025
     * Day 16: Operator Aritmatika Perkalian (*) dan Pembagian (/).
     */

    int a = 10;
    int b = 5;

    // Operator Perkalian
    int perkalian = a * b;

    // Operator Pembagian
    int pembagian = a / b;

    System.out.println(perkalian);
    System.out.println(pembagian);
  }

}
