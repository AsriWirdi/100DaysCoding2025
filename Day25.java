public class Day25 {
  public static void main(String[] args) {

    /*
     * 100 Days Of Coding 2025
     * Day 25: Operator Increment dan Decrement (++, --)
     */

    int i = 0;

    // increment
    i++;
    System.out.println(i);

    // Decrement
    i--;
    System.out.println(i);
  }
}
