public class Day30 {
  public static void main(String[] args) {

    /*
     * 100 Days Of Coding 2025
     * Day 30: Operator Perbandingan <= dan >=.
     */

    int x = 0;
    int y = 1;
    int z = 0;
    System.out.println(x <= y); // true
    System.out.println(x <= z); // true
    System.out.println(x >= y); // false
    System.out.println(x >= z); // true
  }
}