public class Day77 {
  public static void main(String[] args) {
    /*
     * 100 Days Of Coding 2025
     * Day 77 : Method String untuk Manipulasi (substring(), replace(), trim()).
     */

    String nama = " Asri Wirdi ";
    System.out.println(nama.substring(0, 5));

    System.out.println(nama.replace("Asri", "Muh"));

    System.out.println(nama.trim());
  }
}